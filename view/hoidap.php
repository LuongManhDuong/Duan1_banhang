<br> Vào các ngày đặc biệt như lễ tết ngày của mẹ, lễ tình thân, giáng sinh v..v…  bạn có thể tham khảo thêm thông tin về chính sách giao hàng tại thông tin trên website. 

<br> Các địa điểm xa:

<br> Đối với các khu vực xa nội thành đơn hàng giao hàng trong ngày sẽ không được đảm bảo. Việc giao hàng tại các địa điểm lân cận thành phố, hay quá xa <b>ManhDuong</b> sẽ cố gắng hoàn thành sớm nhất có thể để hàng được giao đến đúng thời gian bạn mong muốn.

<br> Các trường hợp bất khả kháng khi giao hàng trong ngày

<br> Đa số các đơn hàng giao gấp cần khoảng 3 giờ để thực hiện. <b>ManhDuong</b> sẽ cố gắng hết sức để  đơn hàng của bạn được giao sớm tuy nhiên chúng tôi không thể cam kết hoàn toàn với đơn hàng giao đến bệnh viên, tòa nhà lớn, chung cư.v.v.. 

