<br> Thông thường là phí giao hàng và phí giữ hàng sẽ bằng không tuy nhiên có thể được tính thêm vào một số đơn hàng đối với một số trường hợp sai địa chỉ giao hàng do phía người đặt như bạn điền sai địa chỉ, số điện thoại. Phụ phí có thể được tinh thêm tùy trường hợp từ 80.000vnd .

