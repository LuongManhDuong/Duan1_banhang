<br> <b>ManhDuong</b> có giao hàng đến bệnh viện không ?

<br> Đa số các bệnh viên thường có quy định cụ thể về việc thăm người bệnh:

<br> Đa số các đơn hàng hoa không thể gửi tận nơi người nhận vì quy định này của bệnh viện và đối với trường hợp này, đơn hàng hoa có thể được gửi đến y tá, bác sĩ và gửi đến người nhận sau đó.

<br> Hoa lily và bong bóng cũng bị cấm tại số số bệnh viện vì khả năng gây dị ứng và không đảm bảo an toàn.

<br> Các loại hoa tươi, cây trồng, hoa quả sẽ không thể được giao đến khu vực chăm sóc đặc biệt tại bệnh viện. 

<br> Vui lòng kiểm tra độ an toàn tại một cơ sở y tế trước khi đặt hàng các sản phẩm đến bệnh viện.

<br> Đối với khoản thời gian trong dịch bệnh nguy hiểm hoặc đang trong thời gian cách ly, <b>ManhDuong</b> có thể từ chối nhận giao hoa tại khu vực bệnh viện.

