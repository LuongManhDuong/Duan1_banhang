                        <b>ManhDuong</b> Chuyên cung cấp dịch vụ hoa tươi tại TP. HCM với các loại hoa đa dạng được tuyển chọn kỹ lưỡng và dịch vụ giao hoa uy tín, nhanh chóng.
<br>
<br>
                        - Nguồn hoa tươi trực tiếp từ mỗi địa phương
<br>
                        Chất lượng hoa tươi luôn được <b>ManhDuong</b> đặt lên hàng đầu. Tại <b>ManhDuong</b> chỉ những bông hoa ưu tú nhất được tuyển chọn để bảo đảm màu sắc tươi tắn và tuổi thọ hoa được lâu. Những bông hoa được sử dụng luôn bảo đảm đạt được tiêu chuẩn vể màu sắc, kích thước, sự đồng đều của các hoa. <b>ManhDuong</b> sử dụng nguồn hoa tại vườn, liên kết với các nguồn hoa uy tín đa dạng về loài hoa, màu sắc và bảo đảm chất lượng cao nhất mà vẫn giữ được mức giá rẻ phù hợp với ngân sách của nhiều đối tượng khách hàng. 
<br>
<br>
                        - Giờ làm việc:
<br>
                        <b>ManhDuong</b> nhận đơn hàng và giao hàng suốt 7 ngày/ tuần – bao gồm cả các ngày lễ với ưu tiêu chí gửi gắm lời yêu thương đến người nhận trong ngày đặc biệt nhanh chóng- tiết kiệm- hiệu quả.  
<br>
<br>
                        - Các dịch vụ tặng kèm và dịch vụ khác
<br>
<br>
                        Mỗi đơn hàng được đặt với <b>ManhDuong</b> đều được tặng kèm thiệp/ banner miễn phí với lời nhắn bất kỳ theo yêu cầu của bạn trong quá trình đặt hàng.
<br>
<br>
                        Ngoài hoa tươi <b>ManhDuong</b> có nhận gửi kèm quà tặng gấu bông và tạo đơn hàng theo yêu cầu (giỏ trái cây, bánh kem v….) tất cả sản phẩm này đều được lựa chọn kỹ lưỡng. Để đặt hàng các sản phẩm không có trong website bạn chỉ cần liên hệ qua email hoặc số điện thoại để được kết nối và tư vấn bởi bộ phận chăm sóc khách hàng của chúng tôi.
<br>
<br>
                        - Dịch vụ chăm sóc khách hàng chuyên nghiệp
<br>
<br>
                        Đội ngũ nhân viên chăm sóc khách hàng tại <b>ManhDuong</b> được đào tạo bài bản với kiến thức rộng về hoa tươi và quà tặng cũng như phong cách hỗ trợ chuyên nghiệp, tận tình để đem lại một trải nghiệp dịch vụ chuyên nghiệp đến khách hàng. Chúng tôi luôn túc trực hỗ trợ bạn trực tuyến trên website, điện thoại, e-mail, có mặt kịp thời để giải đáp thắc mắc và hỗ trợ bạn chu đáo nhất.
<br>
<br>
                        Nếu bạn có bất cứ phản ánh nào về chất lượng sản phẩm, giao hàng chậm trễ...bạn có thể liên hệ trực tuyến, gọi điện hay gửi e-mail cho chúng tôi theo thông tin “Liên hệ” được cung cấp trên website.
<br>
<br>
                        - Chính sách giao hàng trong ngày
<br>
<br>
                        <b>ManhDuong</b> có dịch vụ chuyển phát hoa tươi – quà tặng nhanh trong vòng 3 tiếng (*Tham khảo chi tiết tại “Chính sách giao hàng”)
<br>
<br>
                        Với <b>ManhDuong</b>, bạn sẽ trải nghiệm một dịch vụ chất lượng chuyên nghiệp tuyệt vời nhất.
